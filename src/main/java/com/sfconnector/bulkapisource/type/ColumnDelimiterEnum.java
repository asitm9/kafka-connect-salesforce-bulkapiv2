package com.sfconnector.bulkapisource.type;

public enum ColumnDelimiterEnum {

    /**
     * backquote character (`)
     */
    BACKQUOTE,

    /**
     * caret character (^)
     */
    CARET,

    /**
     * comma character (,) which is the default delimiter
     */
    COMMA,

    /**
     * pipe character (|)
     */
    PIPE,

    /**
     * semicolon character (;)
     */
    SEMICOLON,

    /**
     * tab character
     */
    TAB
}